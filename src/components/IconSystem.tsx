// ============================================
// ICON WRAPPER WITH THEMING
// ============================================

interface ThemedIconProps {
    icon: React.FC<IconProps>;
    mode?: 'institutional' | 'creator';
    className?: string;
    containerClassName?: string;
}

export const ThemedIcon: React.FC<ThemedIconProps> = ({
    icon: Icon,
    mode,
    className = 'w-6 h-6',
    containerClassName = ''
}) => {
    const modeColor = mode === 'institutional'
        ? 'text-institutional'
        : mode === 'creator'
            ? 'text-creator'
            : 'text-foreground';

    return (
        <div className={containerClassName}>
            <Icon className={`${className} ${modeColor}`} mode={mode} />
        </div>
    );
};

